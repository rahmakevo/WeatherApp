import Foundation
import UIKit

extension UIColor {
    static var seaCloudy: UIColor {
        return UIColor(red: 84/255, green: 113/255, blue: 112/255, alpha: 1.0)
    }
    
    static var seaSunny: UIColor {
        return UIColor(red: 37/255, green: 150/255, blue: 190/255, alpha: 1.0)
    }
    
    static var seaRainny: UIColor {
        return UIColor(red: 87/255, green: 87/255, blue: 93/255, alpha: 1.0)
    }
}
